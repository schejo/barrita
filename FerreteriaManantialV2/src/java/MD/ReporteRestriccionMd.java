package MD;

public class ReporteRestriccionMd {

    private String correlativo;
    private String nombre;
    private String precio_uni;
    private String cantidad;
    private String precio_ven;
    private String total; 

    public String getCorrelativo() {
        return correlativo;
    }

    public void setCorrelativo(String correlativo) {
        this.correlativo = correlativo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPrecio_uni() {
        return precio_uni;
    }

    public void setPrecio_uni(String precio_uni) {
        this.precio_uni = precio_uni;
    }

    public String getCantidad() {
        return cantidad;
    }

    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    public String getPrecio_ven() {
        return precio_ven;
    }

    public void setPrecio_ven(String precio_ven) {
        this.precio_ven = precio_ven;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }
    
    
}
